    [super viewDidLoad];
    
    self.flightTypes = @[@"Food", @"Drinks", @"Anything"];
    UITableView *tableView = (id)[self.view viewWithTag:1];
    UIEdgeInsets contentInset = tableView.contentInset;
    contentInset.top = 20;
    [tableView setContentInset:contentInset];
    
